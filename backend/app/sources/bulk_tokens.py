"""Bulk-loaded ATS token candidates across all supported platforms.

The runtime source of truth for ingest is the `sources` table. This
file is the *seed* for migration `0014_bulk_load_ats_tokens` and is
intentionally large — load wide, then let the existing ingest-time
validation + auto-prune machinery (`SOURCE_FAILURE_THRESHOLD`) sort
live boards from dead ones across successive scheduled runs.

Provenance: each list is a hand-curated set of company tokens drawn
from well-known public maintainers of ATS-board indices (e.g. the
many public GitHub repositories that aggregate Greenhouse/Lever
board tokens, the SmartRecruiters company-identifier directory, the
Workday tenant lists that recur in jobs-aggregator open-source
projects). The lists are split per platform because each ATS has
its own token shape:

  * Greenhouse / Lever / Ashby   — lowercase company slug, no
                                   structure (e.g. `stripe`).
  * SmartRecruiters              — case-sensitive identifier
                                   (e.g. `Bosch`); NOT a slug.
  * Workday                      — composite `tenant:dc:site`
                                   triple (e.g. `walmart:wd5:Careers`).
                                   Entries we couldn't form a complete
                                   triple for are deliberately omitted
                                   so the adapter never sees a half-
                                   formed token.

`INGEST_MAX_PER_RUN` (default 150) bounds each ingest pass; with a
few hundred extra source rows landing here, the rotation picks the
least-recently-checked ones first and the full set works through
across several scheduled passes — no single run stalls or runs over
budget.

Adding new tokens: drop them into the matching list below in
alphabetical order. Re-running the migration is a no-op for tokens
already present (idempotent by the `(source_type, token)` unique
constraint).
"""

