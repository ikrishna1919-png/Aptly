def all_bulk_rows() -> dict[str, list[dict]]:
    """Return per-source-type lists of dicts ready for SQL insert. The
    migration assembles its insert payload by concatenating these
    lists; the test suite uses the per-platform breakdown to pin the
    counts."""
    return {
        "greenhouse": [
            {"source_type": "greenhouse", "token": tok, "display_name": name, "location": None}
            for tok, name in GREENHOUSE_BULK_TOKENS
        ],
        "lever": [
            {"source_type": "lever", "token": tok, "display_name": name, "location": None}
            for tok, name in LEVER_BULK_TOKENS
        ],
        "ashby": [
            {"source_type": "ashby", "token": tok, "display_name": name, "location": None}
            for tok, name in ASHBY_BULK_TOKENS
        ],
        "smartrecruiters": [
            {
                "source_type": "smartrecruiters",
                "token": tok,
                "display_name": name,
                "location": None,
            }
            for tok, name in SMARTRECRUITERS_BULK_TOKENS
        ],
        "workday": [
            {"source_type": "workday", "token": tok, "display_name": name, "location": None}
            for tok, name in WORKDAY_BULK_TOKENS
        ],
    }
